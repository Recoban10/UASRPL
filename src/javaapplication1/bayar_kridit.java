    private void tampil(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Cicilan Kode");
        tabel.addColumn("Kredit Kode");
        tabel.addColumn("Cicilan Tanggal");
        tabel.addColumn("Cicilan Jumlah");
        tabel.addColumn("Cicilan Ke");
        tabel.addColumn("Cicilan Sisa Ke");
        tabel.addColumn("Cicilan Harga Ke");        
        tbl_kredit.setModel(tabel);
        try {
            rs=stat.executeQuery("select * from bayar_cicilan order by cicilan_kode");
            while (rs.next())
                tabel.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                rs.getString(6),
                rs.getString(7),
               
            });
                    tbl_kredit.setModel(tabel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
