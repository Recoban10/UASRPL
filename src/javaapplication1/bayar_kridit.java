private void autonumber(){
        try {
            String sql = "select right(cicilan_kode,2)"
                    + "as no_terakhir from "
                    + "bayar_cicilan";
            rs = stat.executeQuery(sql);
            if (rs.first() == false){
                txt_kode.setText("CK0001");
            }else
            rs.last();
            int no = rs.getInt(1) +1;
            String cno = String.valueOf(no);
            int pjg_cno = cno.length();
            int i = 0;
            if (i < 2 - pjg_cno) {
                cno = "000" + cno;
                txt_kode.setText("CK" + cno);
            }else if (i < 3 - pjg_cno){
                cno = "00" + cno;
                txt_kode.setText("CK" + cno);
            }else if (i < 4 - pjg_cno){
                cno = "0" + cno;
                txt_kode.setText("CK" + cno);
            }else if (i < 5 - pjg_cno){
                cno = "" + cno;
                txt_kode.setText("CK" + cno);
            }
            txt_kode.setText("CA" + cno);
        } catch (Exception DBException) {
            System.err.println("error =" + DBException);
        }
    }
